package com.overture.lib.Robots;

import edu.wpi.first.wpilibj.TimedRobot;

/**
 * The OverRobot class
 *
 * <p>OverRobot is a class that extends the TimedRobot class and sets the loop time to the
 * RobotConstants LoopTime.
 */
public class OverRobot extends TimedRobot {
  /** Constructor for OverRobot */
  OverRobot() {
    super(RobotConstants.LoopTime);
  }
}
